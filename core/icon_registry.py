def get_all_icons():
    """Return the full icon registry."""
    return ICON_REGISTRY


def get_icons_by_category(category_slug):
    """Return icons filtered by category."""
    return [i for i in ICON_REGISTRY if i["category"] == category_slug]


def get_icon_choices(style_prefix=True, include_empty=True):
    """
    Return icons as Django choice tuples.

    Args:
        style_prefix: If True, values include style prefix (e.g., 'fas fa-star').
                     If False, values are bare class (e.g., 'fa-star').
        include_empty: If True, include empty choice as first option.
    """
    choices = []
    if include_empty:
        choices.append(("", str(_("--- No Icon ---"))))
    for icon in ICON_REGISTRY:
        value = f"{icon['style']} {icon['class']}" if style_prefix else icon["class"]
        choices.append((value, icon["label"]))
    return choices


def get_registry_as_json():
    """Return the icon registry as a JSON-serializable structure for JS."""
    return {
        "categories": dict(ICON_CATEGORIES),
        "icons": ICON_REGISTRY,
    }
