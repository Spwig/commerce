"""
Marketplace Checkout API Views

Three endpoints for the marketplace purchase flow on spwig.com:
1. GET  /api/marketplace/purchase-token/<uuid>/  — Validate token, return component info
2. POST /api/marketplace/checkout/               — Init checkout, create order + payment intent
3. GET  /api/marketplace/payment-status/<id>/     — Check payment status for confirmation page
"""

import logging
from decimal import Decimal

from django.conf import settings
from django.contrib.auth import get_user_model
from django.core.exceptions import ValidationError as DjangoValidationError
from django.core.validators import validate_email
from django.db import IntegrityError, transaction
from django.utils import timezone
from datetime import timedelta

from rest_framework import status, serializers as drf_serializers
from rest_framework.permissions import AllowAny
from rest_framework.response import Response
from rest_framework.views import APIView
from django.utils.translation import gettext_lazy as _
from drf_spectacular.utils import extend_schema, OpenApiResponse, inline_serializer

from core.api.throttling import MarketplaceCheckoutThrottle, MarketplaceStatusThrottle
from .services import validate_purchase_token

logger = logging.getLogger(__name__)
User = get_user_model()


def _mask_email(email):
    """Mask email for safe logging: 'da***@example.com'."""
    if '@' not in email:
        return '***'
    local, domain = email.split('@', 1)
    masked_local = local[:2] + '***' if len(local) > 2 else '***'
    return f"{masked_local}@{domain}"


class ValidatePurchaseTokenView(APIView):
    """
    Validate a purchase token and return component details.

    Called by the spwig.com frontend when the merchant lands on the purchase page.
    The token was generated by the upgrade server when the merchant clicked "Purchase"
    in their shop admin.
    """
    authentication_classes = []
    permission_classes = [AllowAny]
    throttle_classes = [MarketplaceStatusThrottle]

    @extend_schema(
        tags=['Marketplace'],
        summary=_("Validate purchase token"),
        description=_("Validate a purchase token and return component details. Called by the spwig.com "
                    "frontend when the merchant lands on the purchase page. HQ only."),
        responses={
            200: inline_serializer(
                name='ValidatePurchaseTokenResponse',
                fields={
                    'success': drf_serializers.BooleanField(),
                    'component': drf_serializers.DictField(),
                    'merchant_email': drf_serializers.EmailField(),
                    'merchant_name': drf_serializers.CharField(),
                    'return_url': drf_serializers.URLField(),
                }
            ),
            400: OpenApiResponse(description=_("Invalid or expired purchase token")),
            502: OpenApiResponse(description=_("Unable to validate token with upgrade server")),
        },
    )
    def get(self, request, token_uuid):
        try:
            data = validate_purchase_token(str(token_uuid))
        except ValueError as e:
            # Error messages are already sanitized in services.py
            return Response(
                {'success': False, 'error': str(e)},
                status=status.HTTP_400_BAD_REQUEST
            )
        except Exception as e:
            logger.error(f"Failed to validate purchase token: {e}")
            return Response(
                {'success': False, 'error': 'Unable to validate purchase token'},
                status=status.HTTP_502_BAD_GATEWAY
            )

        return Response({
            'success': True,
            'component': data.get('component', {}),
            'merchant_email': data.get('merchant_email', ''),
            'merchant_name': data.get('merchant_name', ''),
            'return_url': data.get('return_url', ''),
        })


class InitiateMarketplaceCheckoutView(APIView):
    """
    Initialize marketplace checkout.

    Creates a user (if needed), cart, checkout session, and payment intent
    for a marketplace component purchase. Returns embedded payment
    configuration (handler_config + client_secret) for the Airwallex SDK.

    Idempotent: if a valid checkout session already exists for the given
    purchase token, returns the existing payment configuration.
    """
    authentication_classes = []
    permission_classes = [AllowAny]
    throttle_classes = [MarketplaceCheckoutThrottle]

    @extend_schema(
        tags=['Marketplace'],
        summary=_("Initiate marketplace checkout"),
        description=_("Initialize a marketplace component purchase checkout. Creates user (if needed), "
                    "cart, checkout session, and payment intent. Returns embedded payment configuration "
                    "for Airwallex SDK. Idempotent: returns existing checkout if active. HQ only."),
        request=inline_serializer(
            name='MarketplaceCheckoutRequest',
            fields={
                'purchase_token': drf_serializers.UUIDField(help_text='Purchase token from upgrade server'),
                'email': drf_serializers.EmailField(help_text='Buyer email address'),
                'name': drf_serializers.CharField(required=False, help_text='Buyer full name'),
                'billing_country': drf_serializers.CharField(required=False, help_text='ISO country code'),
                'return_base_url': drf_serializers.URLField(required=False, help_text='Base URL for payment return redirects'),
            }
        ),
        responses={
            200: OpenApiResponse(description=_("Existing active checkout session returned (idempotent)")),
            201: inline_serializer(
                name='MarketplaceCheckoutResponse',
                fields={
                    'success': drf_serializers.BooleanField(),
                    'checkout_url': drf_serializers.URLField(),
                    'payment_intent_id': drf_serializers.UUIDField(),
                    'order_number': drf_serializers.CharField(allow_null=True),
                }
            ),
            400: OpenApiResponse(description=_("Invalid purchase token, missing/invalid email, or cart creation failed")),
            500: OpenApiResponse(description=_("No payment provider configured")),
            502: OpenApiResponse(description=_("Unable to validate purchase token with upgrade server")),
        },
    )
    @transaction.atomic
    def post(self, request):
        # Validate required fields
        token_uuid = request.data.get('purchase_token')
        email = request.data.get('email', '').strip().lower()
        name = request.data.get('name', '').strip()
        billing_country = request.data.get('billing_country', '').strip()
        language = request.data.get('language', '').strip() or 'en'

        if not token_uuid:
            return Response(
                {'success': False, 'error': 'purchase_token is required'},
                status=status.HTTP_400_BAD_REQUEST
            )
        if not email:
            return Response(
                {'success': False, 'error': 'email is required'},
                status=status.HTTP_400_BAD_REQUEST
            )

        # Validate email format
        try:
            validate_email(email)
        except DjangoValidationError:
            return Response(
                {'success': False, 'error': 'Invalid email address'},
                status=status.HTTP_400_BAD_REQUEST
            )

        token_uuid_str = str(token_uuid)

        # Idempotency check: return existing active checkout for this purchase token
        from cart.models import CheckoutSession
        from payment_providers.models import PaymentIntent

        existing_session = CheckoutSession.objects.filter(
            metadata__purchase_token=token_uuid_str,
            expires_at__gt=timezone.now(),
        ).first()

        if existing_session:
            active_intent = PaymentIntent.objects.filter(
                checkout_session=existing_session,
                status__in=[
                    'created', 'requires_payment_method',
                    'requires_action', 'processing',
                ],
            ).first()
            if active_intent and (active_intent.client_secret or active_intent.checkout_url):
                from payment_providers.api_serializers import PaymentIntentResponseSerializer
                serialized = PaymentIntentResponseSerializer(active_intent).data
                return Response({
                    'success': True,
                    'checkout_url': active_intent.checkout_url or '',
                    'payment_intent_id': str(active_intent.id),
                    'order_number': (
                        active_intent.order.order_number
                        if active_intent.order else None
                    ),
                    'client_secret': active_intent.client_secret,
                    'handler_config': serialized.get('handler_config'),
                    'sdk_dependencies': serialized.get('sdk_dependencies', []),
                }, status=status.HTTP_200_OK)

        # 1. Validate purchase token with upgrade server
        try:
            token_data = validate_purchase_token(token_uuid_str)
        except ValueError as e:
            # Error messages are already sanitized in services.py
            return Response(
                {'success': False, 'error': str(e)},
                status=status.HTTP_400_BAD_REQUEST
            )
        except Exception as e:
            logger.error(f"Failed to validate purchase token: {e}")
            return Response(
                {'success': False, 'error': 'Unable to validate purchase token'},
                status=status.HTTP_502_BAD_GATEWAY
            )

        component = token_data.get('component', {})
        component_id = component.get('id')
        component_slug = component.get('slug', '')
        component_name = component.get('name', 'Marketplace Component')
        price = Decimal(str(
            component.get('price') or component.get('price_eur') or '0'
        ))
        currency = component.get('currency', 'EUR')
        license_key = token_data.get('license_key', '')
        return_url = token_data.get('return_url', '')

        if not component_id:
            return Response(
                {'success': False, 'error': 'Component ID missing from token data'},
                status=status.HTTP_502_BAD_GATEWAY
            )

        if price <= 0:
            return Response(
                {'success': False, 'error': 'Component has no price'},
                status=status.HTTP_400_BAD_REQUEST
            )

        # 2. Find or create user by email (case-insensitive, race-condition safe)
        try:
            user, user_created = User.objects.get_or_create(
                email__iexact=email,
                defaults={
                    'username': email,
                    'email': email,
                    'first_name': name.split(' ')[0] if name else '',
                    'last_name': ' '.join(name.split(' ')[1:]) if name and ' ' in name else '',
                    'is_active': True,
                }
            )
        except IntegrityError:
            # Race condition: another request created the user concurrently
            user = User.objects.get(email__iexact=email)
            user_created = False

        if user_created:
            user.set_unusable_password()
            user.save(update_fields=['password'])
            logger.info(
                f"Auto-created marketplace user: {_mask_email(email)} "
                f"(user_id={user.pk})"
            )

        # 3. Find or create digital Product for this component
        from catalog.models import Product, Category
        from djmoney.money import Money

        marketplace_category, _ = Category.objects.get_or_create(
            slug='marketplace',
            defaults={'name': 'Marketplace'},
        )

        product, _ = Product.objects.get_or_create(
            sku=f'mkt-{component_id}',
            defaults={
                'name': component_name,
                'slug': f'marketplace-{component_slug}-{component_id}',
                'product_type': 'digital',
                'price': Money(price, currency),
                'status': 'published',
                'track_inventory': False,
                'category': marketplace_category,
            }
        )
        # Update price/name if changed on upgrade server
        update_fields = []
        if product.price.amount != price:
            product.price = Money(price, currency)
            update_fields += ['price', 'price_currency']
        if product.name != component_name:
            product.name = component_name
            update_fields.append('name')
        if update_fields:
            product.save(update_fields=update_fields)

        # 4. Create cart with the product
        from cart.models import Cart
        from cart.services.cart_service import CartService

        cart = Cart.objects.create(user=user)
        success, msg, cart_item = CartService.add_item(
            cart=cart,
            product_id=product.id,
            quantity=1,
        )
        if not success:
            return Response(
                {'success': False, 'error': f'Failed to add to cart: {msg}'},
                status=status.HTTP_400_BAD_REQUEST
            )

        # 5. Create checkout session with marketplace metadata
        session = CheckoutSession.objects.create(
            cart=cart,
            subtotal=product.price,
            total_amount=product.price,
            step_completed='payment',
            metadata={
                'marketplace': True,
                'component_id': component_id,
                'component_slug': component_slug,
                'license_key': license_key,
                'return_url': return_url,
                'purchase_token': token_uuid_str,
                'language': language,
            },
            expires_at=timezone.now() + timedelta(hours=2),
        )

        # 6. Set payment provider
        from payment_providers.models import PaymentProviderAccount

        provider_account = PaymentProviderAccount.objects.filter(
            is_active=True,
            connection_status='connected',
        ).first()

        if not provider_account:
            return Response(
                {'success': False, 'error': 'No payment provider configured'},
                status=status.HTTP_500_INTERNAL_SERVER_ERROR
            )

        session.payment_provider = provider_account
        session.save(update_fields=['payment_provider'])

        # 7. Create payment intent
        from payment_providers.services.payment_orchestration_service import PaymentOrchestrationService

        # Build return URLs for the hosted checkout
        frontend_base = request.data.get('return_base_url', '').rstrip('/')
        if not frontend_base:
            frontend_base = request.build_absolute_uri('/').rstrip('/')

        payment_return_url = f"{frontend_base}/marketplace/purchase/return/"
        payment_cancel_url = f"{frontend_base}/marketplace/purchase/?token={token_uuid_str}&cancelled=true"

        success, intent, message = PaymentOrchestrationService.create_payment_intent(
            checkout_session=session,
            provider_account=provider_account,
            return_url=payment_return_url,
            cancel_url=payment_cancel_url,
            metadata={
                'marketplace': True,
                'component_id': component_id,
                'component_slug': component_slug,
                'license_key': license_key,
            }
        )

        if not success:
            return Response(
                {'success': False, 'error': message},
                status=status.HTTP_400_BAD_REQUEST
            )

        from payment_providers.api_serializers import PaymentIntentResponseSerializer
        serialized = PaymentIntentResponseSerializer(intent).data

        return Response({
            'success': True,
            'checkout_url': intent.checkout_url or '',
            'payment_intent_id': str(intent.id),
            'order_number': intent.order.order_number if intent.order else None,
            'client_secret': intent.client_secret,
            'handler_config': serialized.get('handler_config'),
            'sdk_dependencies': serialized.get('sdk_dependencies', []),
        }, status=status.HTTP_201_CREATED)


class MarketplacePaymentStatusView(APIView):
    """
    Check payment status for a marketplace purchase.

    Called by the spwig.com frontend on the return/confirmation page
    to poll for payment completion. Only returns results for intents
    created within the last 2 hours.
    """
    authentication_classes = []
    permission_classes = [AllowAny]
    throttle_classes = [MarketplaceStatusThrottle]

    @extend_schema(
        tags=['Marketplace'],
        summary=_("Check marketplace payment status"),
        description=_("Check payment status for a marketplace purchase. Called by the spwig.com "
                    "frontend on the return/confirmation page to poll for payment completion. "
                    "Only returns results for recent payment intents (within 2 hours). HQ only."),
        responses={
            200: inline_serializer(
                name='MarketplacePaymentStatusResponse',
                fields={
                    'success': drf_serializers.BooleanField(),
                    'payment_status': drf_serializers.CharField(),
                    'order_number': drf_serializers.CharField(allow_null=True),
                    'order_status': drf_serializers.CharField(allow_null=True),
                    'component_name': drf_serializers.CharField(),
                }
            ),
            404: OpenApiResponse(description=_("Payment intent not found")),
        },
    )
    def get(self, request, intent_id):
        from payment_providers.models import PaymentIntent

        try:
            intent = PaymentIntent.objects.select_related(
                'order', 'provider_account', 'provider_account__component',
            ).get(
                id=intent_id,
                created_at__gte=timezone.now() - timedelta(hours=2),
            )
        except PaymentIntent.DoesNotExist:
            return Response(
                {'success': False, 'error': 'Payment not found'},
                status=status.HTTP_404_NOT_FOUND
            )

        # Verify with provider if not yet terminal (handles delayed/missing webhooks)
        from payment_providers.services.payment_orchestration_service import PaymentOrchestrationService
        PaymentOrchestrationService.verify_and_sync_payment_status(intent)

        order = intent.order
        order_metadata = order.metadata if order else {}

        return Response({
            'success': True,
            'payment_status': intent.status,
            'order_number': order.order_number if order else None,
            'order_status': order.payment_status if order else None,
            'component_name': order_metadata.get('component_slug', '') if order_metadata else '',
        })
